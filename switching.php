<div class="heading2">2.2 Challenges of switching</div>
While there are many advantages of using GNU/Linux, it can also be challenging to switch to something new and different and less mainstream.<br />

<ul>
	<li><b>Learning curve</b> - You'll need to learn how to use a new and quite different operating system and some new applications too- and you'll need to <em>unlearn</em> much of what you've learnt using other operating systems.</li>
	<li><b>Missing applications and games</b> - You may miss familiar applications, typically Microsoft Office, Adobe Photoshop and most popular, big mainstream games. Dual boot, WINE or virtual machines offer partial solutions to this problem. And of course high quality native GNU/Linux alternatives exist.</li>
	<li><b>Lacking hardware support</b> - Most hardware is supported, but not all - before buying new hardware components it's recommended to do a bit of research - the newer and less widespread the components are, the bigger the risk of problems.</li>
	<li><b>Harder to get help</b> - Often friends, family and co-workers won't be able to help you with GNU/Linux related issues, so you'll need to get help online, which is often not as effective as getting help "on-site" from a friend.</li>
</ul>



<div class="heading2">2.3 Strategy</div>
Since switching is not always easy, here's some advise:

<ul>
	<li><b>Be realistic</b> - Do not expect to master GNU/Linux within a week or two, like you've mastered your previous operating system after maybe 10 years of using it or more. And don't expect GNU/Linux to be perfect.</li>
	<li><b>Switch gradually</b> - Start by installing GNU/Linux on a secondary computer or in a dual boot setup with your previous OS or perhaps running in a virtual machine. Start by learning the basics and solve any problems calmly one at a time.</li>
	<li><b>Get help</b> - Don't be afraid to ask for help online or elsewhere.</li>
</ul>

