<div class="heading1">2. Switching to GNU/Linux</div>
Switching to a new and very different operating system is quite an endeavour - of course you don't have to switch entirely, nothing prevents you from using multiple operating systems - even on the same computer. This chapter describes some of the things to consider before venturing into the wonderful world of GNU/Linux.<br /><br />



<div class="heading2">2.1 Why use GNU/Linux?</div>
There are many reasons why millions of people enjoy using GNU/Linux - they can be either of a technical, financial, ethical or philosophical nature depending on each person's personal views. Here's a list of some of the most common reasons for choosing GNU/Linux:<br />

<ul>
	<li><span style="font-weight: bold;">Security</span> - Viruses, spyware etc. are virtually non-existant issues.</li>
      	
	<li><span style="font-weight: bold;">Maintanence</span> - Forget about scanning for viruses and spyware, defragmenting, cleaning registry database, frequent rebooting etc.</li>
        
	<li><span style="font-weight: bold;">Stability</span> - GNU/Linux is very stable. Individual apps might crash, but the operating system itself crashing is very rare.</li>
        
	<li><span style="font-weight: bold;">Software freedom/open source</span> - You may run the software as you wish, study the source code, modify it, share it. No tricky End User License Agreements.</li> 
	
	<li><span style="font-weight: bold;">Open standards</span> -  GNU/Linux and the applications for it, generally support open standards, making it possible to interoperate seamlessly with other platforms and helps you avoid vendor lock-in.</li>
        
	<li><span style="font-weight: bold;">Community</span> - GNU/Linux has been described as a "world wide team sport".</li>
        
	<li><span style="font-weight: bold;">Economy</span> - Most GNU/Linux-distributions can be downloaded gratis and the same goes for a huge amount of applications for it. Furthermore comparatively modest hardware requirements mean you won't need to upgrade your hardware often.</li>
        
	<li><span style="font-weight: bold;">Legality</span> - Getting high quality gratis software legally, means less temptation to use unauthorized illegal copies of proprietary software. You also won't support a well-known monopolist with multiple convictions for abusing its dominant market position.</li>
        
	<li><span style="font-weight: bold;">Transparency</span> - Most GNU/Linux distributions are developed in the open, using public mailinglists, public IRC channels, public bugtrackers, public feature trackers etc.</li>

	<li><span style="font-weight: bold;">Diversity</span> - There are many different GNU/Linux distributions from different vendors or community projects, for different purposes, providing very different user experiences.</li>

	<li><span style="font-weight: bold;">Privacy</span> - Free software will generally respect your privacy, and publicly available source code provides some protection against backdoors and such.</li>

	<li><span style="font-weight: bold;">Trying something new</span> - Simply trying something new and different can in itself be motivating for many people.</li>

	<li>And... <span style="font-weight: bold;">it's a lot of fun!</span></li>
	</ul>

